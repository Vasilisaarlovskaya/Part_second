package Builder;

public class Lighting {
    private String FunctionalLightBulbs;
    private String StreetlightBulbs;

    public void functionalLighting() {

        this.FunctionalLightBulbs = "lamps";
    }

    public void streetLighting() {

        this.StreetlightBulbs = "lanterns";
    }

    public String getFunctionalLightBulbs() {

        return StreetlightBulbs;
    }

    public String getStreetLightBulbs() {

        return StreetlightBulbs;
    }
}
